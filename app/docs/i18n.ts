export const translateLabel = (label: string, language: Language) =>
  language === 'fa' ? (faLabels[label] ?? label) : label;

export const translateUi = (key: keyof typeof faUi, language: Language) =>
  language === 'fa' ? faUi[key] : {
    previous: 'Previous',
    next: 'Next',
    menu: 'Menu',
    closeNavigation: 'Close navigation',
    filterNavigation: 'Filter menu',
    clearFilter: 'Clear filter',
    noFilterResults: 'No results found',
    expand: 'Expand',
    collapse: 'Collapse',
    onThisPage: 'On this page',
    noHeadings: 'No headings',
    language: 'Language',
  }[key];

export const localizeNavigationItem = <T extends NavigationItem>(item: T, language: Language): T => ({
  ...item,
  label: translateLabel(item.label, language),
  status: item.status ? translateLabel(item.status, language) : item.status,
  category: typeof item.category === 'string' ? translateLabel(item.category, language) : item.category,
  children: item.children?.map((child) => localizeNavigationItem(child, language)),
});

export const localizeNavigationItems = <T extends NavigationItem>(items: T[], language: Language): T[] =>
  items.map((item) => localizeNavigationItem(item, language));

export const localizeDocEntry = (doc: DocEntry | undefined, language: Language) =>
  doc ? localizeNavigationItem(doc, language) : undefined;
